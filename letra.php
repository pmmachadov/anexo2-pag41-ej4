Crea una página Web que muestre la tabla de multiplicar de un número indicado
    por el usuario. Cuando el usuario solicite por primera vez la página debe mostrarse un
    formulario semejante el siguiente:
    Con campo de texto y boton MOSTRAR.

    Al pulsar el botón “mostrar” deberá comprobarse que el valor introducido es un
    número comprendido entre 1 y 10 ambos incluidos. En tal caso se muestra debajo la
    tabla de multiplicar correspondiente. En caso contrario, deberá mostrarse un mensaje
    de error explicativo.

    PSEUDO CODIGO

    1.- Inicio
    2.- Pedir numero
    3.- Si numero es mayor que 1 y menor que 10
    4.- Mostrar tabla de multiplicar
    Para ello utilizar un ciclo for.
    5.- Si no
    6.- Mostrar error
    7.- Fin